//피라미드
for (let i = 0; i <= 5; i++) {
  for (let j = 0; j < i; j++) {
    // document.write("*");
    process.stdout.write("*");
  }
  // document.write("<br>");
  console.log("");
}

//피라미드 reverse
for (let i = 0; i <= 5; i++) {
  for (let j = 4; j > i; j--) {
    // document.write("*");
    process.stdout.write("*");
  }
  // document.write("<br>");
  console.log("");
}

